def solve_(mrr,n,m,k):

    depth = [set([x]) for x in range(n)]

    # -1 is the exempt card
    # how many alibi each person has

    for _ in range(k):
        for a,b in mrr:
            if depth[b] == -1:
                continue
            if depth[a] == -1:
                depth[b] = -1
            else:
                for x in depth[a]:
                    depth[b].add(x)
                    if len(depth[b]) > k:
                        depth[b] = -1
                        break

    return n - sum(x != -1 for x in depth)

